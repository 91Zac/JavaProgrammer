package cap03;

public class OperadoresIncrementaisDecrementais {

	public static void main(String[] args) {
		int x = 4;
		System.out.println(x++);
		//aqui o x é igual a 5
		System.out.println(++x);
		//aqui o x é igual a 6
		System.out.println(x--);
		//aqui o x é igual a 5
		System.out.println(--x);
		//aqui o x é igual a 4

	}

}
